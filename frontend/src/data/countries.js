/**
 * Busca paÃ­ses pelo nome (case insensitive)
 * @param {string} query - Texto para buscar
 * @param {number} limit - Limite de resultados (default: 10)
 * @returns {Array} Lista de paÃ­ses que correspondem Ã  busca
 */
export function searchCountries(query, limit = 10) {
  if (!query || query.trim().length === 0) {
    return []
  }
  
  const normalizedQuery = query.toUpperCase().trim()
  
  return countries
    .filter(country => country.name.includes(normalizedQuery))
    .slice(0, limit)
}

/**
 * Busca um paÃ­s pelo nome exato
 * @param {string} name - Nome do paÃ­s
 * @returns {Object|undefined} PaÃ­s encontrado ou undefined
 */
export function getCountryByName(name) {
  if (!name) return undefined
  const normalizedName = name.toUpperCase().trim()
  return countries.find(country => country.name === normalizedName)
}

